public class JavaBasics01{
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        a += b;
        System.out.println("a = " + a);

        a = 5;
        b = 10;
        a -= b;
        System.out.println("a = " + a);
        
        a = 5;
        b = 10;
        a *= b;
        System.out.println("a = " + a);

        a = 5;
        b = 10;
        a /= b;
        System.out.println("a = " + a);
    }
}